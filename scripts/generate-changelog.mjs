#!/usr/bin/env node
/**
 * Auto-changelog generator. Reads `.last-bump.json` (written by bump-version.mjs)
 * and prepends a new entry to CHANGELOG.md, categorized by commit type per the
 * Keep-a-Changelog convention. Idempotent: if the new version is already at the
 * top of CHANGELOG.md, this is a no-op.
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PKG_DIR = resolve(__dirname, "..");
const LAST_BUMP = resolve(PKG_DIR, ".last-bump.json");
const CHANGELOG = resolve(PKG_DIR, "CHANGELOG.md");

const HEADERS = {
  feat: "### Features",
  fix: "### Bug Fixes",
  perf: "### Performance",
  refactor: "### Refactors",
  docs: "### Docs",
  chore: "### Chores",
  build: "### Build",
  ci: "### CI",
  test: "### Tests",
  style: "### Style",
};

const TYPE_RE = /^(feat|fix|chore|docs|refactor|test|perf|ci|build|style)(\([a-z0-9_-]+\))?(!?):\s*(.+)$/;

function buildEntry(rec) {
  const { to, kind, sinceTag, commits } = rec;
  const dateStr = new Date().toISOString().slice(0, 10);
  const buckets = {};
  const breaking = [];
  for (const c of commits) {
    const match = c.subject.match(TYPE_RE);
    if (!match) continue;
    const [, type, , bang, desc] = match;
    if (bang === "!") breaking.push({ ...c, desc });
    const bucket = buckets[type] || [];
    bucket.push({ sha: c.sha, desc });
    buckets[type] = bucket;
  }
  const lines = [];
  lines.push(`## [${to}] — ${dateStr}`);
  lines.push("");
  lines.push(`_${kind.toUpperCase()} bump${sinceTag ? ` from ${sinceTag}` : " (initial automated release)"}_`);
  lines.push("");
  if (breaking.length > 0) {
    lines.push("### BREAKING CHANGES");
    for (const b of breaking) lines.push(`- ${b.desc} (${b.sha})`);
    lines.push("");
  }
  for (const type of Object.keys(HEADERS)) {
    const items = buckets[type];
    if (!items || items.length === 0) continue;
    lines.push(HEADERS[type]);
    for (const it of items) lines.push(`- ${it.desc} (${it.sha})`);
    lines.push("");
  }
  return lines.join("\n");
}

function main() {
  if (!existsSync(LAST_BUMP)) {
    console.error("[changelog] .last-bump.json missing — run bump-version.mjs --apply first");
    process.exit(2);
  }
  const rec = JSON.parse(readFileSync(LAST_BUMP, "utf8"));
  const entry = buildEntry(rec);

  let current = "";
  if (existsSync(CHANGELOG)) current = readFileSync(CHANGELOG, "utf8");

  if (current.includes(`## [${rec.to}]`)) {
    console.log(`[changelog] v${rec.to} already present — no-op`);
    return;
  }

  let header = "# Changelog\n\nAll notable changes to `@trelmir-os/input-quality-compiler` are documented here.\n\nFormat: [Keep a Changelog](https://keepachangelog.com/en/1.1.0/) · Versioning: [SemVer 2.0](https://semver.org/spec/v2.0.0.html) · Auto-generated by `scripts/generate-changelog.mjs` from conventional commits.\n\n";
  let body = current;
  if (current.startsWith("# Changelog")) {
    const idx = current.indexOf("\n## ");
    if (idx >= 0) {
      header = current.slice(0, idx + 1);
      body = current.slice(idx + 1);
    } else {
      header = current.endsWith("\n") ? current : current + "\n";
      body = "";
    }
  }

  const next = `${header}\n${entry}\n${body}`;
  writeFileSync(CHANGELOG, next, "utf8");
  console.log(`[changelog] prepended v${rec.to} (${rec.kind}, ${rec.commits.length} commit(s))`);
}

main();
